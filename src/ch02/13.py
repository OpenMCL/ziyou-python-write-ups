n = int(input())
for i in range(n * 2):
    if i < n // 2:
        print(" " * (2 * n + 2 * n - i - 1) + "/" + "_" * (2 * i) + "\\")
    elif i >= n // 2 and i < n:
        print(
            " " * (2 * n + 2 * n - i - 1)
            + "/"
            + "_" * (2 * i)
            + "\\"
            + " " * (2 * n - i + 2 * n - 1 - n // 2 - (i - n // 2) - 1)
            + "/"
            + "_" * (i - n // 2) * 2
            + "\\"
        )
    else:
        print(
            " " * (2 * n - 1 - i)
            + "/"
            + "_" * (i - n) * 2
            + "\\"
            + " " * (2 * n - 1 - i) * 2
            + "/"
            + "_" * (2 * i)
            + "\\"
            + " " * (2 * n - 1 - i) * 2
            + "/"
            + "_" * (i - n // 2) * 2
            + "\\"
        )
